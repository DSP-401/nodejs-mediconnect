const app = require('./app');

const PORT = 3000;

// Handling Errors
app.use((err, req, res, next) => {
    // console.log(err);
    err.statusCode = err.statusCode || 500;
    err.message = err.message || "Internal Server Error";
    res.status(err.statusCode).json({
      message: err.message,
    });
});

app.listen(PORT,()=> {
    console.log(`Server running on port ${PORT}`);
})